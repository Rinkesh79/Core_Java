package inheritance;

public class Engine {
	
	private String bname;
	private int cc;
	private byte strokes;



	public String getbname()
	{
		return bname;
	}
	
	public void setbname(String bname)
	{
		this.bname=bname;
	}

	public int getcc()
	{
		return cc;
	}
	
	public void setcc(int cc)
	{
		
		this.cc=cc;
	}
	
	public byte getstrokes()
	{
		return strokes;
	}
	
	public void setstrokes(byte strokes)
	{
		this.strokes=strokes;
	}

}
